<script type="text/javascript">
(function ($) {
  $(document).ready(function () {

    /* Event listeners */

    $(".tfft_currency").change(function () {
      tfft_change_currency($(this).val(), $(this).siblings(".tfft_base_amount"));
    });

    $(".tfft_extra_amount").keyup(function () {

      var new_currency, new_total_amount;
      new_currency = $(this).parent().siblings(".tfft_currency").val();
      new_total_amount =
      parseFloat($(this).val()) + parseFloat($(this).parent().siblings('.tfft_base_amount').html());
      tfft_update_hiddens($(this).parent().siblings('form'), new_currency, new_total_amount);
      tfft_update_hiddens_o($(this).parent().siblings('form'), new_currency, new_total_amount);

      if ($(this).val() != "" && (
        $(this).val() < 2 && $(this).parent().siblings('form').children('input[name="t3"]').val() == "M"
        ) || (
          $(this).val() < 24 && $(this).parent().siblings('form').children('input[name="t3"]').val() == "Y"
          )) {
        $(this).parent().siblings('p').children('.error_text').html('The amount you entered is below the minimum donation amount. Please increase your donation.');
        $(this).parent().siblings('form').children('.pp_sub').hide();
      }
      else {
        $(this).parent().siblings('p').children('.error_text').html('');
        $(this).parent().siblings('form').children('.pp_sub').show();
      }

    });

    $("#tfft_button_section .credit_card").click(function () {
      $("#tfft_paypal_section").slideDown(1000);
    });

    /* Functions */

    var tfft_update_hiddens, tfft_update_hiddens_o, tfft_change_currency;

    tfft_update_hiddens = function (form_object, new_currency, new_total_amount) {

      form_object.children("input[name='a3']").val(new_total_amount);
      form_object.children("input[name='currency_code']").val(new_currency);

    };

    tfft_update_hiddens_o = function (form_object, new_currency, new_total_amount) {

      form_object.children("input[name='amount']").val(new_total_amount);
      form_object.children("input[name='currency_code']").val(new_currency);

    };

    tfft_change_currency = function (new_currency, base_amount) {

      var new_base_amount, new_total_amount;

      new_total_amount = parseFloat(base_amount.siblings('p').children('.tfft_extra_amount').val());

      tfft_update_hiddens(base_amount.siblings('form'), new_currency, new_total_amount);
    };

    $('.tfft_extra_amount').val('0');
    $('.tfft_currency').val('GBP');
    tfft_update_hiddens($('#tfft_oneoff form'), 'GBP', '10');
    tfft_update_hiddens($('#tfft_monthly form'), 'GBP', '2');

  });
})(jQuery);
</script>